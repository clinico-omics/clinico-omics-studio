export function reduceSum(metadata, key, value, fieldName, sortBy) {
  let summed = values(
    map(groupBy(metadata, key), (objs, key) => {
      let item = {}
      if (fieldName) {
        item[fieldName] = key
        item['value'] = sumBy(objs, value)
        return item
      } else {
        return {
          key: key,
          value: sumBy(objs, value)
        }
      }
    })
  )

  if (sortBy) {
    return orderBy(summed, sortBy, 'desc')
  } else {
    return orderBy(summed, 'value', 'desc')
  }
}

